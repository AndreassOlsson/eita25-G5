package src.models;

public class MedicalRecord {
    String patientId;
    String doctorId;
    String nurseId;
    String division;
    String data;

    public String getPatientId() {
        return patientId;
    }
    public String getDoctorId() {
        return doctorId;
    }
    public String getNurseId() {
        return nurseId; 
    }
    public String getDivision() {
        return division;
    }
    public String getData() {
        return data;   
    }
    
}
